void ui_run(int* a, int* a_trg, int b)
{
	if(*a < *a_trg){
		*a+=b;
		if(*a > *a_trg)
		   *a = *a_trg;
	}
	if(*a > *a_trg){
		*a-=b;
		if(*a < *a_trg)
		   *a = *a_trg;
	}
	if(*a == *a_trg){
		*a = *a_trg;
	}
}

